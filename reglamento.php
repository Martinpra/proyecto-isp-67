<p>TÍTULO 3: “Ingreso”
Capítulo 1: “De la Inscripción”
Art. 6º: El aspirante que se inscriba en un Instituto de Educación Superior deberá acreditar lo que se indica
en los apartados siguientes, mediante la documentación correspondiente debidamente legalizada y
autenticada:
a.- Su identidad y los datos personales que se le exijan, adjuntando la documentación en respaldo de los
mismos.
b.- La conclusión de sus estudios previos con certificado del Nivel Secundario o equivalente completo.
c.- Certificado médico emitido por organismos públicos o privados.
d.- Toda otra documentación o información que la Dirección de Educación Superior o el Instituto consideren
relevante para la oferta formativa y/o recorrido académico.
e.- Los estudiantes extranjeros que deseen ingresar al Nivel Superior, deberán presentar su certificado de
estudios debidamente legalizado y la documentación personal pertinente, todo conforme a la normativa
vigente.
Se excepciona el requisito del inciso “b” del presente artículo, para aquellas personas mayores de 25 años
que acrediten idoneidad para la carrera elegida ante la Dirección Provincial de Educación Superior.</p>
<p>Art. 7º: La institución educativa brindará información al aspirante respecto de toda la normativa
directamente relacionada con la carrera, aclarándosele que está dando su consentimiento de estar
debidamente informado y asesorado.</p>
<p>Art. 8°: La instrumentación de la inscripción será en formato papel y firmada con carácter de Declaración
Jurada hasta tanto se vaya implementando, con las seguridades legales pertinentes, la forma digital.
<p>Art. 9: La inscripción se considerará definitiva y completa, cuando el ingresante presente toda la
documentación que respalde la Declaración Jurada de los artículos 6º y 8º.</p>
<p>Art. 10: El plazo máximo para dejar la inscripción en la situación descripta en el artículo anterior será el que
fije el Calendario Jurisdiccional oficial correspondiente, o en su defecto el 30 de abril o día hábil inmediato
posterior del año correspondiente al ciclo lectivo en el que el ingresante se inscribe.</p>
<p>Art. 11: Los estudiantes que adeuden Espacios Curriculares del Nivel Secundario tendrán, como plazo
máximo para presentar el certificado o constancia provisoria de finalización de estudios Secundarios, hasta
el último día hábil de ese ciclo lectivo.</p>
<p>Art. 12: La inscripción tendrá carácter de condición resolutoria hasta su cumplimiento. Esto significa que si
el ingresante no cumple con los requisitos exigidos en el presente Capítulo, no adquiere derecho alguno
emergente de su Trayectoria.
El estudiante no podrá acceder a los exámenes finales de la/s Unidad/es Curricular/es que correspondieran
hasta la presentación de la documentación exigida.</p>

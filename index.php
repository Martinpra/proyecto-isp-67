   </p> 

<p>Desde las primeras clases que comenzaron a dictarse en julio de 2014 hasta la actualidad, el instituto fue creciendo rápidamente sustentado en una impronta que combinaba el sentido comunitario y humanista de la enseñanza con excelencia académica y profesionalismo. 
La limitada capacidad edilicia y los condicionantes administrativos hicieron que la demanda de inscriptos, superase con creces a la oferta posible, expresando claramente la importancia y la necesidad de este tipo de instituciones educativas en los barrios santafesinos
</p>
